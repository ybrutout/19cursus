#include "minishell.h"

void	free_env(char ** env, int nb)
{
	if (nb > 0)
	{
		if (nb > 1)
		{
			while (nb > 1)
			{
				free(env[nb - 2]);
				nb--;
			}
		}
		free(env);
	}
}
